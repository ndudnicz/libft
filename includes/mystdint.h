#ifndef MYSTDINT_H
# define MYSTDINT_H

typedef char		t_s8;
typedef short		t_s16;
typedef int			t_s32;
typedef long int	t_s64;

typedef unsigned char		t_u8;
typedef unsigned short		t_u16;
typedef unsigned int		t_u32;
typedef unsigned long int	t_u64;

#endif
